

\section{Die HBS-Erwerbspersonenbefragung}



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
  
  
  \begin{frame}[fragile]{Die HBS-Erwerbspersonenbefragung}

\begin{itemize}
\setlength\itemsep{6pt}
\onslide<1->
  \item[\textcolor{hbsorange}{\textbullet}] Wer wird befragt?
  \begin{itemize} 
\item[\textcolor{hbsorange}{\textbullet}] Erwerbspersonen in Deutschland
\item[\textcolor{hbsorange}{\textbullet}] Ursprünglich 7.677 befragte Personen
\item[\textcolor{hbsorange}{\textbullet}] Fünf weitere \glqq Wellen\grqq\xspace mit mindestens 5.047 befragten Personen
\end{itemize}
\onslide<2->  
  \item[\textcolor{hbsorange}{\textbullet}] Was wird gefragt?
  \begin{itemize} 
\item[\textcolor{hbsorange}{\textbullet}] Wiederholt: Arbeitssituation, Einstellungen, Sorgen, ...
\item[\textcolor{hbsorange}{\textbullet}] Einzeln: Arbeitszeiten, Konsumausgaben, Wahlentscheidung, ...
\end{itemize}
\onslide<3->
  \item[\textcolor{hbsorange}{\textbullet}] Wie wird gefragt?
  \begin{itemize} 
\item[\textcolor{hbsorange}{\textbullet}] Online-Panel, offline rekrutiert
\item[\textcolor{hbsorange}{\textbullet}] Quotenstichprobe
\item[\textcolor{hbsorange}{\textbullet}] Maßnahmen der Qualitätssicherung, z.B. Ausschluss auffälliger Interviews 
\end{itemize}

\end{itemize}


\end{frame}

